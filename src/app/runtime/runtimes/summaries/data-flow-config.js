export function createSummariesDataFlowConfig({
  pageConfig,
  selectors,
  dashboardState,
  text,
  defaultSettings,
  autoRefreshIntervalMs,
  runAfterDomAndIdle,
  setDatasetValue,
  setStatus,
  fetchData,
  perfMonitor,
  describeCacheMeta,
  describeError,
  computeDailyStats,
  getDefaultChartFilters,
  computeMonthlyStats,
  computeYearlyStats,
  renderYearlyTable,
  numberFormatter,
  getSettings,
  getClientConfig,
  getAutoRefreshTimerId,
  setAutoRefreshTimerId,
}) {
  return {
    pageConfig,
    selectors,
    dashboardState,
    TEXT: text,
    DEFAULT_SETTINGS: defaultSettings,
    AUTO_REFRESH_INTERVAL_MS: autoRefreshIntervalMs,
    uiHooks: {
      runAfterDomAndIdle,
      setDatasetValue,
      setStatus,
      getSettings,
      getClientConfig,
      getAutoRefreshTimerId,
      setAutoRefreshTimerId,
    },
    chartHooks: {
      getDefaultChartFilters,
    },
    dataHooks: {
      fetchData,
      perfMonitor,
      describeCacheMeta,
      describeError,
      computeDailyStats,
      filterDailyStatsByWindow: (daily) => (Array.isArray(daily) ? daily : []),
      computeMonthlyStats,
      computeYearlyStats,
      renderYearlyTable,
    },
    numberFormatter,
  };
}
