export function createChartsDataFlowConfig({
  pageConfig,
  selectors,
  dashboardState,
  text,
  defaultSettings,
  autoRefreshIntervalMs,
  runAfterDomAndIdle,
  setDatasetValue,
  setStatus,
  showChartSkeletons,
  fetchData,
  perfMonitor,
  describeCacheMeta,
  describeError,
  computeDailyStats,
  filterDailyStatsByWindow,
  mergeDailyStatsSeries,
  populateChartYearOptions,
  populateChartsHospitalTableYearOptions,
  populateHourlyCompareYearOptions,
  populateHeatmapYearOptions,
  syncHeatmapFilterControls,
  getDefaultChartFilters,
  sanitizeChartFilters,
  kpiFilterLabels,
  syncChartFilterControls,
  prepareChartDataForPeriod,
  renderChartsPrimary,
  renderChartsSecondary,
  renderCharts,
  renderChartsHospitalTable,
  getHeatmapData,
  onChartsPrimaryVisible,
  scheduleChartsSecondaryRender,
  numberFormatter,
  getSettings,
  getClientConfig,
  getAutoRefreshTimerId,
  setAutoRefreshTimerId,
}) {
  return {
    pageConfig,
    selectors,
    dashboardState,
    TEXT: text,
    DEFAULT_SETTINGS: defaultSettings,
    AUTO_REFRESH_INTERVAL_MS: autoRefreshIntervalMs,
    uiHooks: {
      runAfterDomAndIdle,
      setDatasetValue,
      setStatus,
      getSettings,
      getClientConfig,
      getAutoRefreshTimerId,
      setAutoRefreshTimerId,
    },
    chartHooks: {
      showChartSkeletons,
      populateChartYearOptions,
      populateChartsHospitalTableYearOptions,
      populateHourlyCompareYearOptions,
      populateHeatmapYearOptions,
      syncHeatmapFilterControls,
      getDefaultChartFilters,
      sanitizeChartFilters,
      KPI_FILTER_LABELS: kpiFilterLabels,
      syncChartFilterControls,
      prepareChartDataForPeriod,
      renderChartsPrimary,
      renderChartsSecondary,
      renderCharts,
      renderChartsHospitalTable,
      getHeatmapData,
      onChartsPrimaryVisible,
      scheduleChartsSecondaryRender,
    },
    dataHooks: {
      fetchData,
      perfMonitor,
      describeCacheMeta,
      describeError,
      computeDailyStats,
      filterDailyStatsByWindow,
      mergeDailyStatsSeries,
    },
    numberFormatter,
  };
}
